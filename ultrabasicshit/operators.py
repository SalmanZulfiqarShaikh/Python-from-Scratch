a = 5
b = 69


print("Addition:", a + b)
print("Subtraction:", b - a)
print("Multiplication:", a * b)
print("Division:" , b / a)
print("Floor Division:", b // a)
print("Modulus:", b % a)
print("Exponentiation:", a ** 2)
# Comparison Operators
print("Is a equal to b?", a == b)
print("Is a not equal to b?", a != b)
print("Is a greater than b?", a > b)
print("Is a less than b?", a < b)
print("Is a greater than or equal to b?", a >= b)
print("Is a less than or equal to b?", a <= b)
# Logical Operators
print("a > 2 and b < 100:", a > 2 and b < 100)
print("a < 2 or b < 100:", a < 2 or b < 100)
print("not (a > 2):", not (a > 2))
# Assignment Operators
c = a  # Simple assignment
c += b  # c = c + b
print("Value of c after c += b:", c)
c -= a  # c = c - a
print("Value of c after c -= a:", c)
c *= 2  # c = c * 2
print("Value of c after c *= 2:", c)
c /= 3  # c = c / 3
print("Value of c after c /= 3:", c)
c %= 4  # c = c % 4
print("Value of c after c %= 4:", c)
c **= 2  # c = c ** 2
print("Value of c after c **= 2:", c)
